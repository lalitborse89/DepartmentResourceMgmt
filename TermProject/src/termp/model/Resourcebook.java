package termp.model;

public class Resourcebook {
	String bookernetid, resourceid, from, to, id;

	public String getBookernetid() {
		return bookernetid;
	}

	public void setBookernetid(String bookernetid) {
		this.bookernetid = bookernetid;
	}

	public String getResourceid() {
		return resourceid;
	}

	public void setResourceid(String resourceid) {
		this.resourceid = resourceid;
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
	
}
