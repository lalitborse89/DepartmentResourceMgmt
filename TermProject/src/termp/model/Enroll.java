package termp.model;

public class Enroll {
	String enrollid;
	String teachesid;
	String studentnetd;
	String credit;
	public String getEnrollid() {
		return enrollid;
	}
	public void setEnrollid(String enrollid) {
		this.enrollid = enrollid;
	}
	public String getTeachesid() {
		return teachesid;
	}
	public void setTeachesid(String teachesid) {
		this.teachesid = teachesid;
	}
	public String getStudentnetd() {
		return studentnetd;
	}
	public void setStudentnetd(String studentnetd) {
		this.studentnetd = studentnetd;
	}
	public String getCredit() {
		return credit;
	}
	public void setCredit(String credit) {
		this.credit = credit;
	}
	

}
