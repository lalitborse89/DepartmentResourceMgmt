package termp.model;

public class Course {
	String courseid;
	String coursename;
	String category;
	String basicsyllabus;
	int credit;
	String isvariablecredit;
	public String getCourseid() {
		return courseid;
	}
	public void setCourseid(String courseid) {
		this.courseid = courseid;
	}
	public String getCoursename() {
		return coursename;
	}
	public void setCoursename(String coursename) {
		this.coursename = coursename;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getBasicsyllabus() {
		return basicsyllabus;
	}
	public void setBasicsyllabus(String basicsyllabus) {
		this.basicsyllabus = basicsyllabus;
	}
	public int getCredit() {
		return credit;
	}
	public void setCredit(int credit) {
		this.credit = credit;
	}
	public String getIsvariablecredit() {
		return isvariablecredit;
	}
	public void setIsvariablecredit(String isvariablecredit) {
		this.isvariablecredit = isvariablecredit;
	}

}
